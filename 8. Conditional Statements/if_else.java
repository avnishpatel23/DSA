// Print if a number is odd or even

import java.util.*;

public class if_else {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        if (num % 2 == 0){
          System.out.println("EVEN");
        }
        else {
            System.out.println("ODD");
        }
    }
}    